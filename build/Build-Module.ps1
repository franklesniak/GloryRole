[CmdletBinding()]
param(
    [Parameter()]
    [string]$SourcePath = (Join-Path -Path (Split-Path -Path $PSScriptRoot -Parent) -ChildPath 'src'),

    [Parameter()]
    [string]$OutputRoot = (Join-Path -Path (Split-Path -Path $PSScriptRoot -Parent) -ChildPath 'out'),

    [Parameter()]
    [string]$ModuleName = 'GloryRole'
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

if (-not (Test-Path -Path $SourcePath)) {
    throw "Source path not found: $SourcePath"
}

$resolvedSourcePath = (Resolve-Path -Path $SourcePath).Path

$moduleOutputPath = Join-Path -Path $OutputRoot -ChildPath $ModuleName

if (Test-Path -Path $moduleOutputPath) {
    Remove-Item -Path $moduleOutputPath -Recurse -Force
}

New-Item -Path $moduleOutputPath -ItemType Directory -Force | Out-Null

# Exclude Invoke-RoleMiningPipeline.ps1 from concatenation.
# It is a script-style entry point (param block at script scope, not wrapped
# in a function block). Including it in the .psm1 would execute the param
# block and pipeline body at import time. It will be refactored into a
# function in a future phase; until then it is copied separately.
$sourceFiles = Get-ChildItem -Path $resolvedSourcePath -Filter '*.ps1' -File |
    Where-Object { $_.Name -ne 'Invoke-RoleMiningPipeline.ps1' } |
    Sort-Object -Property Name

if ($sourceFiles.Count -eq 0) {
    throw "No source scripts were found in $resolvedSourcePath"
}

$psm1Path = Join-Path -Path $moduleOutputPath -ChildPath "$ModuleName.psm1"

$builder = New-Object System.Text.StringBuilder

[void]$builder.AppendLine("# ---------------------------------------------------------------")
[void]$builder.AppendLine("# GloryRole.psm1 - Auto-generated by Build-Module.ps1")
[void]$builder.AppendLine("# Do not edit manually; modify individual files in src/ instead.")
[void]$builder.AppendLine("# ---------------------------------------------------------------")
[void]$builder.AppendLine("Set-StrictMode -Version Latest")
[void]$builder.AppendLine()

foreach ($file in $sourceFiles) {
    $content = Get-Content -Path $file.FullName -Raw
    # Strip any leading UTF-8 BOM (U+FEFF) so the Set-StrictMode regex
    # below matches reliably regardless of source file encoding.
    $content = $content.TrimStart([char]0xFEFF)
    # Strip Set-StrictMode lines from individual files (already in the header)
    $content = $content -replace '(?m)^\s*Set-StrictMode\s+-Version\s+\S+\s*$', ''
    [void]$builder.AppendLine("#region $($file.Name)")
    [void]$builder.AppendLine($content.TrimEnd())
    [void]$builder.AppendLine("#endregion $($file.Name)")
    [void]$builder.AppendLine()
}

$objUtf8NoBomEncoding = New-Object System.Text.UTF8Encoding($false)
[System.IO.File]::WriteAllText($psm1Path, $builder.ToString(), $objUtf8NoBomEncoding)

# Copy the committed module manifest
$psd1Source = Join-Path -Path $resolvedSourcePath -ChildPath "$ModuleName.psd1"
$psd1Destination = Join-Path -Path $moduleOutputPath -ChildPath "$ModuleName.psd1"
if (Test-Path -Path $psd1Source) {
    Copy-Item -Path $psd1Source -Destination $psd1Destination
} else {
    throw "Module manifest not found at $psd1Source"
}

# Copy the pipeline entry-point script as a standalone tool
$pipelineScript = Join-Path -Path $resolvedSourcePath -ChildPath 'Invoke-RoleMiningPipeline.ps1'
if (Test-Path -Path $pipelineScript) {
    Copy-Item -Path $pipelineScript -Destination $moduleOutputPath
}

# Copy LICENSE
$licensePath = Join-Path -Path (Split-Path -Path $PSScriptRoot -Parent) -ChildPath 'LICENSE'
if (Test-Path -Path $licensePath) {
    Copy-Item -Path $licensePath -Destination $moduleOutputPath
}

# Copy samples
$samplesPath = Join-Path -Path (Split-Path -Path $PSScriptRoot -Parent) -ChildPath 'samples'
if (Test-Path -Path $samplesPath) {
    Copy-Item -Path $samplesPath -Destination $moduleOutputPath -Recurse
}

# Validate the manifest loads
$manifest = Test-ModuleManifest -Path $psd1Destination
Write-Information -MessageData "Built module: $($manifest.Name) v$($manifest.Version)" -InformationAction Continue
Write-Information -MessageData "  $psm1Path" -InformationAction Continue
Write-Information -MessageData "  $psd1Destination" -InformationAction Continue
